import Onyx from 'react-native-onyx';
import ONYXKEYS from '../../ONYXKEYS';

function invalidateAuthToken() {
    Onyx.merge(ONYXKEYS.SESSION, {authToken: 'invalid'});
}

function invalidateCredentials() {
    Onyx.merge(ONYXKEYS.CREDENTIALS, {autoGeneratedLogin: 'fake', autoGeneratedPassword: 'password'});
}

export {
    invalidateCredentials,
    invalidateAuthToken,
};

