import Button from '@components/ButtonComposed';
import HeaderWithBackButton from '@components/HeaderWithBackButton';
import OfflineWithFeedback from '@components/OfflineWithFeedback';
import RenderHTML from '@components/RenderHTML';
import ScreenWrapper from '@components/ScreenWrapper';
import Text from '@components/Text';

import useLocalize from '@hooks/useLocalize';
import useOnyx from '@hooks/useOnyx';
import useThemeStyles from '@hooks/useThemeStyles';

import {clearRevokeError, revokeDevice} from '@libs/actions/User';
import Navigation from '@libs/Navigation/Navigation';
import {getDeviceDisplayName, getDeviceLogins, getLastLogin, getLoginKey} from '@libs/UserUtils';

import CONST from '@src/CONST';
import ONYXKEYS from '@src/ONYXKEYS';
import type {Credentials} from '@src/types/onyx';
import type {Login} from '@src/types/onyx/Logins';

import type {ListRenderItemInfo} from '@shopify/flash-list';
import type {OnyxEntry} from 'react-native-onyx';

import {FlashList} from '@shopify/flash-list';
import React from 'react';
import {View} from 'react-native';

function autoGeneratedLoginSelector(credentials: OnyxEntry<Credentials>) {
    return credentials?.autoGeneratedLogin;
}

function DeviceManagementPage() {
    const styles = useThemeStyles();
    const {translate, datetimeToRelative} = useLocalize();

    const [logins] = useOnyx(ONYXKEYS.LOGINS, {selector: getDeviceLogins});
    const [autoGeneratedLogin] = useOnyx(ONYXKEYS.CREDENTIALS, {selector: autoGeneratedLoginSelector});

    const listHeader = (
        <View style={[styles.pv3]}>
            <RenderHTML html={translate('deviceManagementPage.description')} />
        </View>
    );

    const renderItem = ({item}: ListRenderItemInfo<Login>) => {
        const {deviceName, deviceVersion, os, osVersion} = item.additionalData ?? {};
        const displayName = getDeviceDisplayName(item, deviceName, deviceVersion, os, osVersion, translate('deviceManagementPage.unknownDevice'));
        return (
            <OfflineWithFeedback
                pendingAction={item.pendingAction}
                errors={item.errorFields?.revoke}
                onClose={() => clearRevokeError(getLoginKey(item))}
                contentContainerStyle={[styles.flexRow, styles.alignItemsCenter, styles.pv3, styles.gap3]}
            >
                <View style={[styles.flex1, styles.flexColumn, styles.gap1]}>
                    <Text style={[styles.textLabelSupporting]}>{datetimeToRelative(getLastLogin(item))}</Text>
                    <Text>{displayName}</Text>
                </View>
                <Button
                    variant="danger"
                    size={CONST.BUTTON_SIZE.SMALL}
                    onPress={() => revokeDevice(item, autoGeneratedLogin)}
                >
                    <Button.Text>{translate('deviceManagementPage.revoke')}</Button.Text>
                </Button>
            </OfflineWithFeedback>
        );
    };

    return (
        <ScreenWrapper
            includeSafeAreaPaddingBottom
            testID="DeviceManagementPage"
        >
            <HeaderWithBackButton
                title={translate('deviceManagementPage.title')}
                onBackButtonPress={Navigation.goBack}
            />
            <FlashList
                data={logins}
                renderItem={renderItem}
                keyExtractor={getLoginKey}
                maintainVisibleContentPosition={{disabled: true}}
                contentContainerStyle={[styles.ph5]}
                ListHeaderComponent={listHeader}
            />
        </ScreenWrapper>
    );
}

export default DeviceManagementPage;
