import {Str} from 'expensify-common';
import {Linking} from 'react-native';
import type {OnyxEntry, OnyxMultiSetInput} from 'react-native-onyx';
import Onyx from 'react-native-onyx';
import * as AppActions from '@libs/actions/App';
import {hasAuthToken} from '@libs/actions/Session';
import * as Session from '@libs/actions/Session';
import {getCurrentUserEmail, setLastShortAuthToken} from '@libs/Network/NetworkStore';
import App from '@src/App';
import CONST from '@src/CONST';
import ONYXKEYS from '@src/ONYXKEYS';
import ROUTES from '@src/ROUTES';
import {createRandomReport} from '../utils/collections/reports';
import PusherHelper from '../utils/PusherHelper';
import * as TestHelper from '../utils/TestHelper';
import waitForBatchedUpdatesWithAct from '../utils/waitForBatchedUpdatesWithAct';
import waitForNetworkPromises from '../utils/waitForNetworkPromises';
import wrapOnyxWithWaitForBatchedUpdates from '../utils/wrapOnyxWithWaitForBatchedUpdates';

jest.mock('@libs/BootSplash', () => ({
    hide: jest.fn().mockResolvedValue(undefined),
}));

const TEST_USER_ACCOUNT_ID_1 = 123;
const TEST_USER_LOGIN_1 = 'test@test.com';
// cspell:disable-next-line
const TEST_AUTH_TOKEN_1 = 'asdfghjkl';

const TEST_USER_ACCOUNT_ID_2 = 456;
const TEST_USER_LOGIN_2 = 'test2@test.com';
// cspell:disable-next-line
const TEST_AUTH_TOKEN_2 = 'zxcvbnm';

// We need a large timeout here as we are lazy loading React Navigation screens and this test is running against the entire mounted App
jest.setTimeout(120000);
TestHelper.setupApp();
TestHelper.setupGlobalFetchMock();

const report = createRandomReport(7, undefined);

// cspell:disable-next-line
const TEST_SUPPORT_AUTH_TOKEN = 'supporttoken123';

function getInitialURL() {
    const params = new URLSearchParams();

    params.set('exitTo', `${ROUTES.REPORT}/${report.reportID}`);
    params.set('email', TEST_USER_LOGIN_1);
    params.set('shortLivedAuthToken', TEST_AUTH_TOKEN_1);

    const deeplinkUrl = `${CONST.DEEPLINK_BASE_URL}/transition?${params.toString()}`;
    return deeplinkUrl;
}

function getSupportAuthURL() {
    const params = new URLSearchParams();

    params.set('email', TEST_USER_LOGIN_1);
    params.set('delegatorEmail', TEST_USER_LOGIN_2);
    params.set('shortLivedAuthToken', TEST_SUPPORT_AUTH_TOKEN);
    params.set('authTokenType', CONST.AUTH_TOKEN_TYPES.SUPPORT);

    const deeplinkUrl = `${CONST.DEEPLINK_BASE_URL}/transition?${params.toString()}`;
    return deeplinkUrl;
}

describe('Deep linking', () => {
    let lastVisitedPath: string | undefined;
    let lastVisitedPathConnectionID: ReturnType<typeof Onyx.connect> | undefined;
    let originalSignInWithShortLivedAuthToken: typeof Session.signInWithShortLivedAuthToken;

    beforeAll(() => {
        originalSignInWithShortLivedAuthToken = Session.signInWithShortLivedAuthToken;
    });

    beforeEach(() => {
        jest.restoreAllMocks();
        wrapOnyxWithWaitForBatchedUpdates(Onyx);

        lastVisitedPathConnectionID = Onyx.connect({
            key: ONYXKEYS.LAST_VISITED_PATH,
            callback: (val: OnyxEntry<string>) => (lastVisitedPath = val),
        });

        // Set session state directly via multiSet rather than waiting for the API.read response
        // (optimistic→network→finally Onyx cycle) to keep the test fast. Then call through to
        // the real signInWithShortLivedAuthToken so it sets NetworkStore.lastShortAuthToken —
        // the token-cache guard in LogInWithShortLivedAuthTokenPage reads that value to skip
        // re-authentication after sign-out, which is exactly what the second test asserts.
        jest.spyOn(Session, 'signInWithShortLivedAuthToken').mockImplementation(() => {
            Onyx.multiSet({
                [ONYXKEYS.CREDENTIALS]: {
                    login: TEST_USER_LOGIN_1,
                    autoGeneratedLogin: Str.guid('expensify.cash-'),
                    autoGeneratedPassword: Str.guid(),
                },
                [ONYXKEYS.ACCOUNT]: {validated: true, isUsingExpensifyCard: false},
                [ONYXKEYS.PERSONAL_DETAILS_LIST]: {
                    [TEST_USER_ACCOUNT_ID_1]: TestHelper.buildPersonalDetails(TEST_USER_LOGIN_1, TEST_USER_ACCOUNT_ID_1, 'Test'),
                },
                [ONYXKEYS.SESSION]: {
                    authToken: TEST_AUTH_TOKEN_1,
                    accountID: TEST_USER_ACCOUNT_ID_1,
                    email: TEST_USER_LOGIN_1,
                    encryptedAuthToken: TEST_AUTH_TOKEN_1,
                },
                [ONYXKEYS.NVP_PRIVATE_PUSH_NOTIFICATION_ID]: 'randomID',
            });
            return originalSignInWithShortLivedAuthToken(TEST_AUTH_TOKEN_1);
        });

        // Set the keys the app needs to finish loading rather than going through
        // getPolicyParamsForOpenOrReconnect + writeWithNoDuplicatesConflictAction(OPEN_APP),
        // which adds a policy-collection read, and a full
        // request-queue round-trip with optimistic→network→finally Onyx data.
        // NVP_ONBOARDING suppresses the onboarding flow that would otherwise render
        // extra screens and inflate the React work queue drained by act().
        // Single multiSet keeps this to one batched-update cycle. The report collection key
        // is cast because multiSet's type only covers non-computed keys.
        jest.spyOn(AppActions, 'openApp').mockImplementation(() =>
            Onyx.multiSet({
                [`${ONYXKEYS.COLLECTION.REPORT}${report.reportID}`]: report,
                [ONYXKEYS.IS_LOADING_APP]: false,
                [ONYXKEYS.IS_LOADING_REPORT_DATA]: false,
                [ONYXKEYS.HAS_LOADED_APP]: true,
                [ONYXKEYS.NVP_ONBOARDING]: {hasCompletedGuidedSetupFlow: true},
            } as unknown as OnyxMultiSetInput),
        );
    });

    afterEach(async () => {
        if (lastVisitedPathConnectionID) {
            Onyx.disconnect(lastVisitedPathConnectionID);
            lastVisitedPathConnectionID = undefined;
        }
        cleanup();
        await act(async () => {
            await Onyx.clear();
        });
        await waitForBatchedUpdatesWithAct();
        PusherHelper.teardown();
        jest.clearAllMocks();
        lastVisitedPath = undefined;
        Linking.setInitialURL('');
        setLastShortAuthToken(null);
    });

    // This test renders the full App and processes a deep-link sign-in flow, so it runs
    // significantly longer than the suite default on loaded CI runners.
    it(
        'should not remember the report path of the last deep link login after signing out and in again',
        async () => {
            expect(hasAuthToken()).toBe(false);

            const url = getInitialURL();
            // User signs in automatically when the app is rendered because of the deep link
            Linking.setInitialURL(url);
            const {unmount} = render(<App />);

            await waitForBatchedUpdatesWithAct();

            const reportPath = `/${ROUTES.REPORT}/${report.reportID}`;
            expect(decodeURIComponent(lastVisitedPath ?? '')).toContain(reportPath);

            expect(hasAuthToken()).toBe(true);

            await TestHelper.signOutTestUser();
            await waitForBatchedUpdatesWithAct();
            await waitForNetworkPromises();

            expect(hasAuthToken()).toBe(false);

            await TestHelper.signInWithTestUser(TEST_USER_ACCOUNT_ID_2, TEST_USER_LOGIN_2, undefined, TEST_AUTH_TOKEN_2);

            await waitForBatchedUpdatesWithAct();

            expect(lastVisitedPath).toBeDefined();
            expect(decodeURIComponent(lastVisitedPath ?? '')).not.toContain(reportPath);

            unmount();
            await waitForBatchedUpdatesWithAct();
            await waitForNetworkPromises();
        },
        4 * 60 * 1000,
    );

    // This test renders the full App three times, so it runs significantly longer than
    // the suite default on loaded CI runners.
    it(
        'should not reuse the last deep link and log in again when signing out',
        async () => {
            expect(hasAuthToken()).toBe(false);

            const {unmount: unmount1} = render(<App />);
            await TestHelper.signInWithTestUser(TEST_USER_ACCOUNT_ID_2, TEST_USER_LOGIN_2, undefined, TEST_AUTH_TOKEN_2);

            await waitForBatchedUpdatesWithAct();

            expect(hasAuthToken()).toBe(true);
            expect(getCurrentUserEmail()).toBe(TEST_USER_LOGIN_2);
            // Unmount so we can prepare the deep link login
            unmount1();

            await waitForBatchedUpdatesWithAct();
            await waitForNetworkPromises();

            const url = getInitialURL();
            // User signs in automatically when the app is remounted because of the deep link.
            // This overrides the previous sign-in.
            Linking.setInitialURL(url);
            const {unmount: unmount2} = render(<App />);

            await waitForBatchedUpdatesWithAct();

            expect(getCurrentUserEmail()).toBe(TEST_USER_LOGIN_1);

            await TestHelper.signOutTestUser();
            await waitForBatchedUpdatesWithAct();
            await waitForNetworkPromises();

            // In a failing scenario, remounting triggers the sign-in with the deep link again because it still remembers it.
            // However, we've implemented a fix so that it does not reuse the last deep link.
            unmount2();
            await waitForBatchedUpdatesWithAct();
            await waitForNetworkPromises();

            const {unmount: unmount3} = render(<App />);

            await waitForBatchedUpdatesWithAct();

            expect(hasAuthToken()).toBe(false);

            unmount3();
            await waitForBatchedUpdatesWithAct();
            await waitForNetworkPromises();
        },
        4 * 60 * 1000,
    );
});

describe('Support auth token login', () => {
    let originalSignInWithSupportAuthToken: typeof Session.signInWithSupportAuthToken;

    beforeAll(() => {
        originalSignInWithSupportAuthToken = Session.signInWithSupportAuthToken;
    });

    beforeEach(() => {
        jest.restoreAllMocks();

        jest.spyOn(Session, 'signInWithSupportAuthToken').mockImplementation((authToken: string) => {
            Onyx.merge(ONYXKEYS.CREDENTIALS, {
                login: TEST_USER_LOGIN_1,
                autoGeneratedLogin: Str.guid('expensify.cash-'),
                autoGeneratedPassword: Str.guid(),
            });
            Onyx.merge(ONYXKEYS.ACCOUNT, {
                validated: true,
                isUsingExpensifyCard: false,
            });
            Onyx.merge(ONYXKEYS.PERSONAL_DETAILS_LIST, {
                [TEST_USER_ACCOUNT_ID_1]: TestHelper.buildPersonalDetails(TEST_USER_LOGIN_1, TEST_USER_ACCOUNT_ID_1, 'Test'),
            });
            Onyx.merge(ONYXKEYS.SESSION, {
                authToken,
                authTokenType: CONST.AUTH_TOKEN_TYPES.SUPPORT,
                accountID: TEST_USER_ACCOUNT_ID_1,
                email: TEST_USER_LOGIN_1,
                encryptedAuthToken: authToken,
            });
            Onyx.merge(ONYXKEYS.NVP_PRIVATE_PUSH_NOTIFICATION_ID, 'randomID');

            return originalSignInWithSupportAuthToken(authToken);
        });
    });

    afterEach(async () => {
        await Onyx.clear();
        await waitForNetworkPromises();
        jest.clearAllMocks();
        Linking.setInitialURL('');
        setLastShortAuthToken(null);
    });

    it('should not call signInWithSupportAuthToken twice when navigating from LogInWithShortLivedAuthTokenPage to LogOutPreviousUserPage', async () => {
        // First, sign in a user so the app is on AuthScreens
        const {unmount: unmount1} = render(<App />);
        await TestHelper.signInWithTestUser(TEST_USER_ACCOUNT_ID_2, TEST_USER_LOGIN_2, undefined, TEST_AUTH_TOKEN_2);

        await waitForBatchedUpdatesWithAct();

        expect(hasAuthToken()).toBe(true);
        expect(getCurrentUserEmail()).toBe(TEST_USER_LOGIN_2);
        unmount1();

        await waitForBatchedUpdatesWithAct();

        // Now set up the support auth token deep link and remount.
        // With an existing delegator session, the app renders AuthScreens (LogOutPreviousUserPage).
        const url = getSupportAuthURL();
        Linking.setInitialURL(url);
        const {unmount: unmount2} = render(<App />);

        await waitForBatchedUpdatesWithAct();

        expect(Session.signInWithSupportAuthToken).toHaveBeenCalledWith(TEST_SUPPORT_AUTH_TOKEN);
        expect(Session.signInWithSupportAuthToken).toHaveBeenCalledTimes(1);

        unmount2();
        await waitForBatchedUpdatesWithAct();

        // Clear auth and remount with the same transition URL to emulate PublicScreens
        // (LogInWithShortLivedAuthTokenPage) processing the already-handled support token.
        // The duplicate call guard should skip signing in with the same token again.
        await Onyx.merge(ONYXKEYS.SESSION, {authToken: null});
        const {unmount: unmount3} = render(<App />);

        await waitForBatchedUpdatesWithAct();

        expect(Session.signInWithSupportAuthToken).toHaveBeenCalledTimes(1);

        unmount3();
        await waitForBatchedUpdatesWithAct();
    });
});
