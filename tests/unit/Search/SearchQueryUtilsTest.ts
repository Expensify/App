const personalDetailsFakeData = {
    'johndoe@example.com': {
        accountID: 12345,
    },
    'janedoe@example.com': {
        accountID: 78901,
    },
} as Record<string, {accountID: number}>;

const countToken = (query: string, token: string) => query.split(' ').filter((segment) => segment === token).length;

jest.mock('@libs/PersonalDetailsUtils', () => {
    return {
        getPersonalDetailByEmail(email: string) {
            return personalDetailsFakeData[email];
        },
    };
});

// The default query is generated by default values from parser, which are defined in grammar.
// We don't want to test or mock the grammar and the parser, so we're simply defining this string directly here.
const defaultQuery = `type:expense sortBy:date sortOrder:desc`;

describe('SearchQueryUtils', () => {
    describe('getQueryWithUpdatedValues', () => {
        test('returns default query for empty value', () => {
            const userQuery = '';

            const result = getQueryWithUpdatedValues(userQuery);

            expect(result).toEqual(defaultQuery);
        });

        test('returns query with updated amounts', () => {
            const userQuery = 'foo test amount:20000';

            const result = getQueryWithUpdatedValues(userQuery);

            expect(result).toEqual(`foo test amount:2000000 ${defaultQuery}`);
        });

        test('returns query with user emails substituted', () => {
            const userQuery = 'from:johndoe@example.com hello';

            const result = getQueryWithUpdatedValues(userQuery);

            expect(result).toEqual(`from:12345 hello ${defaultQuery}`);
        });

        test('returns query with user emails substituted and preserves user ids', () => {
            const userQuery = 'from:johndoe@example.com to:112233';

            const result = getQueryWithUpdatedValues(userQuery);

            expect(result).toEqual(`from:12345 to:112233 ${defaultQuery}`);
        });

        test('returns query with all of the fields correctly substituted', () => {
            const userQuery = 'from:9876,87654 to:janedoe@example.com hello amount:150 test';

            const result = getQueryWithUpdatedValues(userQuery);

            expect(result).toEqual(`from:9876,87654 to:78901 hello amount:15000 test ${defaultQuery}`);
        });

        test('returns query with updated groupBy', () => {
            const userQuery = 'from:johndoe@example.com groupBy:reports';

            const result = getQueryWithUpdatedValues(userQuery);

            expect(result).toEqual(`from:12345 groupBy:reports ${defaultQuery}`);
        });
    });

    describe('buildQueryStringFromFilterFormValues', () => {
        test('simple filter value', () => {
            const filterValues: Partial<SearchAdvancedFiltersForm> = {
                type: 'expense',
                status: CONST.SEARCH.STATUS.EXPENSE.ALL,
                policyID: ['12345'],
                amountLessThan: '100',
            };

            const result = buildQueryStringFromFilterFormValues(filterValues);

            expect(result).toEqual('sortBy:date sortOrder:desc type:expense policyID:12345 amount<100');
        });

        test('with Policy ID', () => {
            const filterValues: Partial<SearchAdvancedFiltersForm> = {
                policyID: ['12345'],
            };

            const result = buildQueryStringFromFilterFormValues(filterValues);

            expect(result).toEqual('sortBy:date sortOrder:desc policyID:12345');
        });

        test('with keywords', () => {
            const filterValues: Partial<SearchAdvancedFiltersForm> = {
                type: 'expense',
                status: CONST.SEARCH.STATUS.EXPENSE.ALL,
                policyID: ['67890'],
                merchant: 'Amazon',
                description: 'Electronics',
                keyword: 'laptop',
                category: ['electronics', 'gadgets'],
            };

            const result = buildQueryStringFromFilterFormValues(filterValues);

            expect(result).toEqual('sortBy:date sortOrder:desc type:expense policyID:67890 merchant:Amazon description:Electronics laptop category:electronics,gadgets');
        });

        test('currencies and categories', () => {
            const filterValues: Partial<SearchAdvancedFiltersForm> = {
                type: 'expense',
                status: CONST.SEARCH.STATUS.EXPENSE.ALL,
                category: ['services', 'consulting'],
                currency: ['USD', 'EUR'],
            };

            const result = buildQueryStringFromFilterFormValues(filterValues);

            expect(result).toEqual('sortBy:date sortOrder:desc type:expense category:services,consulting currency:USD,EUR');
        });

        test('preserves existing order when base position info provided', () => {
            const baseQuery = 'status:drafts from:johndoe@example.com type:expense';
            const baseQueryJSON = buildSearchQueryJSON(baseQuery);
            const filterValues: Partial<SearchAdvancedFiltersForm> = {
                type: CONST.SEARCH.DATA_TYPES.EXPENSE,
                status: CONST.SEARCH.STATUS.EXPENSE.DRAFTS,
                from: ['johndoe@example.com'],
            };

            const result = buildQueryStringFromFilterFormValues(filterValues, {basePositionInfo: baseQueryJSON?.positionInfo ?? []});

            expect(result.startsWith('sortBy:date sortOrder:desc')).toBe(true);
            expect(result.indexOf('status:drafts')).toBeGreaterThan(result.indexOf('sortOrder:desc'));
            expect(result.indexOf('status:drafts')).toBeLessThan(result.indexOf('from:johndoe@example.com'));
            expect(result.indexOf('from:johndoe@example.com')).toBeLessThan(result.indexOf('type:expense'));
        });

        test('has empty category values', () => {
            const filterValues: Partial<SearchAdvancedFiltersForm> = {
                type: 'expense',
                status: CONST.SEARCH.STATUS.EXPENSE.ALL,
                category: ['equipment', 'consulting', 'none,Uncategorized'],
            };

            const result = buildQueryStringFromFilterFormValues(filterValues);

            expect(result).toEqual('sortBy:date sortOrder:desc type:expense category:equipment,consulting,none,Uncategorized');
        });

        test('empty filter values', () => {
            const filterValues: Partial<SearchAdvancedFiltersForm> = {};

            const result = buildQueryStringFromFilterFormValues(filterValues);

            expect(result).toEqual('sortBy:date sortOrder:desc');
        });

        test('array of from', () => {
            const filterValues: Partial<SearchAdvancedFiltersForm> = {
                type: 'expense',
                from: ['user1@gmail.com', 'user2@gmail.com'],
                to: ['user3@gmail.com'],
            };
            const result = buildQueryStringFromFilterFormValues(filterValues);

            expect(result).toEqual('sortBy:date sortOrder:desc type:expense from:user1@gmail.com,user2@gmail.com to:user3@gmail.com');
        });

        test('complex filter values', () => {
            const filterValues: Partial<SearchAdvancedFiltersForm> = {
                type: 'expense',
                from: ['user1@gmail.com', 'user2@gmail.com'],
                to: ['user3@gmail.com'],
                dateAfter: '2025-03-01',
                dateBefore: '2025-03-10',
                amountLessThan: '1000',
                amountGreaterThan: '1',
                category: ['finance', 'insurance'],
            };
            const result = buildQueryStringFromFilterFormValues(filterValues);

            expect(result).toEqual(
                'sortBy:date sortOrder:desc type:expense from:user1@gmail.com,user2@gmail.com to:user3@gmail.com category:finance,insurance date>2025-03-01 date<2025-03-10 amount>1 amount<1000',
            );
            expect(result).not.toMatch(CONST.VALIDATE_FOR_HTML_TAG_REGEX);
        });

        test('total filter values', () => {
            const filterValues: Partial<SearchAdvancedFiltersForm> = {
                type: 'expense',
                totalLessThan: '1000',
                totalGreaterThan: '1',
            };
            const result = buildQueryStringFromFilterFormValues(filterValues);

            expect(result).toEqual('sortBy:date sortOrder:desc type:expense total>1 total<1000');
        });

        test('equal to filter values', () => {
            const filterValues: Partial<SearchAdvancedFiltersForm> = {
                type: 'expense',
                amountEqualTo: '500',
                totalEqualTo: '750',
            };
            const result = buildQueryStringFromFilterFormValues(filterValues);

            expect(result).toEqual('sortBy:date sortOrder:desc type:expense amount:500 total:750');
        });

        test('combined equal to and range filter values', () => {
            const filterValues: Partial<SearchAdvancedFiltersForm> = {
                type: 'expense',
                amountEqualTo: '100',
                totalGreaterThan: '50',
                totalLessThan: '200',
            };
            const result = buildQueryStringFromFilterFormValues(filterValues);

            expect(result).toEqual('sortBy:date sortOrder:desc type:expense amount:100 total>50 total<200');
        });

        test('with withdrawal type filter', () => {
            const filterValues: Partial<SearchAdvancedFiltersForm> = {
                type: 'expense',
                status: CONST.SEARCH.STATUS.EXPENSE.ALL,
                withdrawalType: CONST.SEARCH.WITHDRAWAL_TYPE.EXPENSIFY_CARD,
            };

            const result = buildQueryStringFromFilterFormValues(filterValues);

            expect(result).toEqual('sortBy:date sortOrder:desc type:expense withdrawalType:expensify-card');
        });

        test('with withdrawn filter', () => {
            const filterValues: Partial<SearchAdvancedFiltersForm> = {
                type: 'expense',
                status: CONST.SEARCH.STATUS.EXPENSE.ALL,
                withdrawnOn: CONST.SEARCH.DATE_PRESETS.LAST_MONTH,
            };

            const result = buildQueryStringFromFilterFormValues(filterValues);

            expect(result).toEqual('sortBy:date sortOrder:desc type:expense withdrawn:last-month');
        });
    });

    describe('buildFilterFormValuesFromQuery', () => {
        test('category filter includes empty values', () => {
            const policyID = generatePolicyID();
            const queryString = 'sortBy:date sortOrder:desc type:expense category:none,Uncategorized,Maintenance';
            const queryJSON = buildSearchQueryJSON(queryString);

            const policyCategories = {
                [`${ONYXKEYS.COLLECTION.POLICY_CATEGORIES}${policyID}`]: {
                    Maintenance: {
                        enabled: true,
                        name: 'Maintenance',
                    },
                    Travel: {
                        enabled: true,
                        name: 'Travel',
                    },
                    Meals: {
                        enabled: true,
                        name: 'Meals',
                    },
                },
            };
            const policyTags = {};
            const currencyList = {};
            const personalDetails = {};
            const cardList = {};
            const reports = {};
            const taxRates = {};

            if (!queryJSON) {
                throw new Error('Failed to parse query string');
            }

            const result = buildFilterFormValuesFromQuery(queryJSON, policyCategories, policyTags, currencyList, personalDetails, cardList, reports, taxRates);

            expect(result).toEqual({
                type: 'expense',
                status: CONST.SEARCH.STATUS.EXPENSE.ALL,
                category: ['Maintenance', 'none,Uncategorized'],
            });
        });
    });

    describe('shouldHighlight', () => {
        it('returns false if either input is empty', () => {
            expect(shouldHighlight('', 'test')).toBe(false);
            expect(shouldHighlight('Some text', '')).toBe(false);
        });

        it('matches exact word at beginning', () => {
            expect(shouldHighlight('Take a 2-minute tour', 'Take')).toBe(true);
        });

        it('matches exact word in middle', () => {
            expect(shouldHighlight('Take a 2-minute tour', '2-minute')).toBe(true);
        });

        it('matches phrase with leading space', () => {
            expect(shouldHighlight('Take a 2-minute tour', ' 2-minute tour')).toBe(true);
        });

        it('matches with special characters', () => {
            // cspell:disable-next-line
            expect(shouldHighlight('Explore the #%tự đặc biệt!', '#%tự')).toBe(true);
        });

        it('is case-insensitive', () => {
            expect(shouldHighlight('Take a 2-minute tour', 'TOUR')).toBe(true);
        });

        it('does not match partial word in the middle', () => {
            expect(shouldHighlight('Take a 2-minute tour', 'in')).toBe(false);
        });

        it('does not match incomplete trailing text', () => {
            expect(shouldHighlight('Take a 2-minute tour', '2-minute to')).toBe(false);
        });

        it('matches multi-word phrase exactly', () => {
            expect(shouldHighlight('Take a 2-minute tour', '2-minute tour')).toBe(true);
        });

        it('does not match words out of order', () => {
            expect(shouldHighlight('Take a 2-minute tour', 'tour 2-minute')).toBe(false);
        });
    });

    describe('sortOptionsWithEmptyValue', () => {
        it('should prioritize empty values at the start', () => {
            const options = ['B', 'A', CONST.SEARCH.CATEGORY_EMPTY_VALUE, 'C'];
            const sortedOptions = options.sort((a, b) => sortOptionsWithEmptyValue(a, b, localeCompare));

            expect(sortedOptions).toEqual([CONST.SEARCH.CATEGORY_EMPTY_VALUE, 'A', 'B', 'C']);
        });

        it('should sort non-empty values properly', () => {
            const options = ['B', 'A', 'C'];
            const sortedOptions = options.sort((a, b) => sortOptionsWithEmptyValue(a, b, localeCompare));

            expect(sortedOptions).toEqual(['A', 'B', 'C']);
        });
    });

    describe('similarSearchHash', () => {
        it('should return same similarSearchHash for two queries that are the same but use different sorting', () => {
            const queryJSONa = buildSearchQueryJSON('sortBy:date sortOrder:desc type:expense category:none,Uncategorized,Maintenance');
            const queryJSONb = buildSearchQueryJSON('sortBy:date sortOrder:asc type:expense category:none,Uncategorized,Maintenance');

            expect(queryJSONa?.similarSearchHash).toEqual(queryJSONb?.similarSearchHash);
        });

        it('should return same similarSearchHash for two queries that have same filters but different values', () => {
            const queryJSONa = buildSearchQueryJSON('sortBy:date sortOrder:desc type:expense feed:"oauth.americanexpressfdx.com 1001" posted:last-statement');
            const queryJSONb = buildSearchQueryJSON('sortBy:date sortOrder:desc type:expense feed:"1234_stripe" posted:last-month');

            expect(queryJSONa?.similarSearchHash).toEqual(queryJSONb?.similarSearchHash);
        });

        it('should return same similarSearchHash for queries with a date range', () => {
            const queryJSONa = buildSearchQueryJSON('type:expense withdrawal-type:reimbursement withdrawn:last-month');
            const queryJSONb = buildSearchQueryJSON('type:expense withdrawal-type:reimbursement withdrawn>2025-01-01 withdrawn<2025-01-03');

            expect(queryJSONa?.similarSearchHash).toEqual(queryJSONb?.similarSearchHash);
        });

        it('should return different similarSearchHash for two queries that have different types', () => {
            const queryJSONa = buildSearchQueryJSON('sortBy:date sortOrder:desc type:expense feed:"oauth.americanexpressfdx.com 1001"');
            const queryJSONb = buildSearchQueryJSON('sortBy:date sortOrder:desc type:trip feed:"oauth.americanexpressfdx.com 1001"');

            expect(queryJSONa?.similarSearchHash).not.toEqual(queryJSONb?.similarSearchHash);
        });
    });

    describe('Query Order Preservation', () => {
        it('should preserve the original query order when position information is available', () => {
            const originalQuery = 'type:expense group-by:merchant status:draft';
            const queryJSON = buildSearchQueryJSON(originalQuery);

            if (!queryJSON) {
                throw new Error('Failed to parse query string');
            }

            const reconstructedQuery = buildSearchQueryString(queryJSON);

            // The reconstructed query should maintain the same order as the original
            // Note: parser normalizes values (group-by → groupBy, draft → drafts)
            expect(reconstructedQuery).toEqual('type:expense groupBy:merchant status:drafts sortBy:date sortOrder:desc');
        });

        it('should preserve order with different key arrangements', () => {
            const originalQuery = 'status:draft type:expense group-by:merchant';
            const queryJSON = buildSearchQueryJSON(originalQuery);

            if (!queryJSON) {
                throw new Error('Failed to parse query string');
            }

            const reconstructedQuery = buildSearchQueryString(queryJSON);

            // Status should come first in the reconstruction since it appeared first in the original
            expect(reconstructedQuery).toEqual('status:drafts type:expense groupBy:merchant sortBy:date sortOrder:desc');
        });

        it('should keep filters in their typed position', () => {
            const originalQuery = 'status:done from:john type:expense';
            const queryJSON = buildSearchQueryJSON(originalQuery);

            if (!queryJSON) {
                throw new Error('Failed to parse query string');
            }

            const reconstructedQuery = buildSearchQueryString(queryJSON);

            expect(reconstructedQuery).toEqual('status:done from:john type:expense sortBy:date sortOrder:desc');
        });

        it('should preserve repeated filter tokens in order', () => {
            const originalQuery = 'from:name1 from:name2 type:expense';
            const queryJSON = buildSearchQueryJSON(originalQuery);

            if (!queryJSON) {
                throw new Error('Failed to parse query string');
            }

            const reconstructedQuery = buildSearchQueryString(queryJSON);

            expect(reconstructedQuery).toEqual('from:name1 from:name2 type:expense sortBy:date sortOrder:desc');
        });

        it('should drop duplicate root entries while keeping the first occurrence', () => {
            const originalQuery = 'type:expense type:expense status:draft';
            const queryJSON = buildSearchQueryJSON(originalQuery);

            if (!queryJSON) {
                throw new Error('Failed to parse query string');
            }

            const reconstructedQuery = buildSearchQueryString(queryJSON);

            expect(reconstructedQuery).toEqual('type:expense status:drafts sortBy:date sortOrder:desc');
        });

        it('should drop duplicate filter entries while keeping the first occurrence', () => {
            const originalQuery = 'from:john from:john type:expense';
            const queryJSON = buildSearchQueryJSON(originalQuery);

            if (!queryJSON) {
                throw new Error('Failed to parse query string');
            }

            const reconstructedQuery = buildSearchQueryString(queryJSON);

            expect(reconstructedQuery).toEqual('from:john type:expense sortBy:date sortOrder:desc');
        });

        it('should dedupe duplicates in parsed query state', () => {
            const originalQuery = 'action:submit action:submit type:expense';
            const queryJSON = buildSearchQueryJSON(originalQuery);

            if (!queryJSON) {
                throw new Error('Failed to parse query string');
            }

            const actionFilters = queryJSON.flatFilters.find((filterGroup) => filterGroup.key === CONST.SEARCH.SYNTAX_FILTER_KEYS.ACTION);
            expect(actionFilters?.filters).toHaveLength(1);
            const actionPositions = queryJSON.positionInfo?.filter((entry) => entry.key === CONST.SEARCH.SYNTAX_FILTER_KEYS.ACTION) ?? [];
            expect(actionPositions).toHaveLength(1);
        });

        it('should collapse duplicate values for the same filter', () => {
            const originalQuery = 'action:submit action:submit type:expense';
            const queryJSON = buildSearchQueryJSON(originalQuery);

            if (!queryJSON) {
                throw new Error('Failed to parse query string');
            }

            const reconstructedQuery = buildSearchQueryString(queryJSON);

            expect(reconstructedQuery).toEqual('action:submit type:expense sortBy:date sortOrder:desc');
        });

        it('should keep different values for the same filter key in order', () => {
            const originalQuery = 'action:submit action:approve type:expense';
            const queryJSON = buildSearchQueryJSON(originalQuery);

            if (!queryJSON) {
                throw new Error('Failed to parse query string');
            }

            const reconstructedQuery = buildSearchQueryString(queryJSON);

            expect(reconstructedQuery).toEqual('action:submit action:approve type:expense sortBy:date sortOrder:desc');
        });

        it('should test position ordering with complex queries', () => {
            const originalQuery = 'group-by:merchant amount:100 type:expense status:draft from:name';
            const queryJSON = buildSearchQueryJSON(originalQuery);

            if (!queryJSON) {
                throw new Error('Failed to parse query string');
            }

            // Verify positions are always in ascending order (important for optimization)
            const positions = queryJSON.positionInfo?.map((p) => p.position) ?? [];
            const isInOrder = positions.every((pos, i) => i === 0 || pos > (positions.at(i - 1) ?? 0));
            expect(isInOrder).toBe(true); // This ensures no sorting is needed
        });

        it('should fallback to default behavior when no position information is available', () => {
            const queryJSON = buildSearchQueryJSON('type:expense group-by:merchant status:draft');

            if (!queryJSON) {
                throw new Error('Failed to parse query string');
            }

            // Manually remove position info to test fallback
            delete queryJSON.positionInfo;

            const reconstructedQuery = buildSearchQueryString(queryJSON);

            // Should still work but may use default ordering
            expect(reconstructedQuery).toContain('type:expense');
            expect(reconstructedQuery).toContain('groupBy:merchant');
            expect(reconstructedQuery).toContain('status:drafts');
        });

        it('should return default query string when called without arguments', () => {
            const reconstructedQuery = buildSearchQueryString();

            expect(reconstructedQuery).toEqual('type:expense sortBy:date sortOrder:desc');
        });

        it('should preserve order in buildUserReadableQueryString (used by UI)', () => {
            const originalQuery = 'status:draft type:expense group-by:merchant';
            const queryJSON = buildSearchQueryJSON(originalQuery);

            if (!queryJSON) {
                throw new Error('Failed to parse query string');
            }

            // Mock the dependencies for buildUserReadableQueryString
            const mockPersonalDetails = {};
            const mockReports = {};
            const mockTaxRates = {};
            const mockCardList = {};
            const mockCardFeeds = {};
            const mockPolicies = {};
            const mockCurrentUserAccountID = 1;

            const userReadableQuery = buildUserReadableQueryString(
                queryJSON,
                mockPersonalDetails,
                mockReports,
                mockTaxRates,
                mockCardList,
                mockCardFeeds,
                mockPolicies,
                mockCurrentUserAccountID,
            );

            // Status should come first since it appeared first in the original query
            // Note: buildUserReadableQueryString doesn't normalize values like buildSearchQueryString does
            expect(userReadableQuery).toEqual('status:draft type:expense group-by:merchant');
        });

        it('should preserve filter order in buildUserReadableQueryString', () => {
            const originalQuery = 'status:draft from:john type:expense';
            const queryJSON = buildSearchQueryJSON(originalQuery);

            if (!queryJSON) {
                throw new Error('Failed to parse query string');
            }

            const mockPersonalDetails = {};
            const mockReports = {};
            const mockTaxRates = {};
            const mockCardList = {};
            const mockCardFeeds = {};
            const mockPolicies = {};
            const mockCurrentUserAccountID = 1;

            const userReadableQuery = buildUserReadableQueryString(
                queryJSON,
                mockPersonalDetails,
                mockReports,
                mockTaxRates,
                mockCardList,
                mockCardFeeds,
                mockPolicies,
                mockCurrentUserAccountID,
            );

            expect(userReadableQuery).toEqual('status:draft from:john type:expense');
        });

        it('should drop duplicate segments in buildUserReadableQueryString', () => {
            const originalQuery = 'type:expense type:expense from:john from:john action:submit action:submit';
            const queryJSON = buildSearchQueryJSON(originalQuery);

            if (!queryJSON) {
                throw new Error('Failed to parse query string');
            }

            const mockPersonalDetails = {};
            const mockReports = {};
            const mockTaxRates = {};
            const mockCardList = {};
            const mockCardFeeds = {};
            const mockPolicies = {};
            const mockCurrentUserAccountID = 1;

            const userReadableQuery = buildUserReadableQueryString(
                queryJSON,
                mockPersonalDetails,
                mockReports,
                mockTaxRates,
                mockCardList,
                mockCardFeeds,
                mockPolicies,
                mockCurrentUserAccountID,
            );

            expect(countToken(userReadableQuery, 'type:expense')).toBe(1);
            expect(countToken(userReadableQuery, 'from:john')).toBe(1);
            expect(countToken(userReadableQuery, 'action:submit')).toBe(1);
        });

        it('should retain different values for the same filter in buildUserReadableQueryString', () => {
            const originalQuery = 'type:expense action:submit action:approve';
            const queryJSON = buildSearchQueryJSON(originalQuery);

            if (!queryJSON) {
                throw new Error('Failed to parse query string');
            }

            const mockPersonalDetails = {};
            const mockReports = {};
            const mockTaxRates = {};
            const mockCardList = {};
            const mockCardFeeds = {};
            const mockPolicies = {};
            const mockCurrentUserAccountID = 1;

            const userReadableQuery = buildUserReadableQueryString(
                queryJSON,
                mockPersonalDetails,
                mockReports,
                mockTaxRates,
                mockCardList,
                mockCardFeeds,
                mockPolicies,
                mockCurrentUserAccountID,
            );

            expect(countToken(userReadableQuery, 'type:expense')).toBe(1);
            expect(countToken(userReadableQuery, 'action:submit')).toBe(1);
            expect(countToken(userReadableQuery, 'action:approve')).toBe(1);
        });
    });
});
